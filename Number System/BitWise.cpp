#include<iostream>

using namespace std;

int main(){
    cout<<"6 AND 10: " <<(6 & 10) <<endl;
    cout<<"6 OR 10: " << (6 | 10) <<endl;
    cout<<"6 XOR 10: " << (6 ^ 10) <<endl;
    cout<<"10 << 2: " << (10<<2) <<endl;
    cout<<"10 >> 1: " << (10>>1) <<endl;
    cout<<"7 >> 1: " << (7>>1) <<endl;

    return 0;
}